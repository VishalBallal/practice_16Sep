a=20
b=14

def addition(a,b):
    return a+b
print(f'Addition of {a} and {b} is {addition(a,b)}')

def substraction(a,b):
    return a-b
print(f'Substraction of {a} and {b} is {substraction(a,b)}')

def multiplication(a,b):
    return a*b
print(f'Multiplication of {a} and {b} is {multiplication(a,b)}')
